<template>
<div>
    <h3>Añade tus intereses:</h3>
    <input
    v-model="nuevoInteres"
    @keyup.enter="agregarInteres"
    placeholder="Escribe un interés y presiona Enter"
    />
    <div class="chips">
    <span v-for="(interes, index) in intereses" :key="index" class="chip">
        {{ interes }}
        <button @click="eliminarInteres(index)">x</button>
    </span>
    </div>
</div>
</template>

<script>
export default {
    data() {
    return {
    nuevoInteres: "",
    ntereses: [],
    };
},
    methods: {
    agregarInteres() {
        if (this.nuevoInteres.trim() !== "") {
        this.intereses.push(this.nuevoInteres.trim());
        this.nuevoInteres = "";
        }
    },
    eliminarInteres(index) {
    this.intereses.splice(index, 1);
    },
},
};
</script>

