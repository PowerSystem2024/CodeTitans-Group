#Repaso Diccionarios
diccionarioNuevo = {
    "Azul": "Blue", 
    "Rojo": "Red", 
    "Verde": "Green", 
    "Amarillo": "Yellow"
    }
print(diccionarioNuevo)

#Como eliminar un elemento del diccionario
del (diccionarioNuevo["Azul"])
print(diccionarioNuevo)

#En los diccionarios se pueden almacenar diferentes tipos de datos
diccionario2 = {
    "Leandro" : {
        "Edad" : 37,
        "Altura" : 1.80
    },
    "Agustin" : [30, 1.85],
    "Ines" : [70, 1.60]
}
print(diccionario2)